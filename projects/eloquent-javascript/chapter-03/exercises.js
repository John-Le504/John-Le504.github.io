// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  //lowercase char
  char.toLowerCase();
  //lowercase everything and split str into array
  const arrOfStr = str.toLowerCase().split("");
  
  //loop through str using reduce
  return arrOfStr.reduce((counter, currentChar) => {
    //if currentChar equals char,increment counter
    if(currentChar === char){
      //increment counter
      counter++;
    }
    //return counter
    return counter;
  }, 0);
}

