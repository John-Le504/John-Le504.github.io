function min(x, y) {
  //if x is greater than y
  if(x > y){
    //return y
    return y;
  }
  //else return x
  else return x;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  
  //base case
  //if n is 0, return true
  if(n === 0){
    return true;
  }
  //if n is 1 or -1, return odd
  if(n === 1 || n === -1){
    return false;
  }
  //recursive case
  //if n is negative
  if(n < 0){
    //return recursive call of unEven with n + 2
    return isEven(n + 2);
  }
  //else n is positive
  else{
    //return recursive call of unEven with n - 2
    return isEven(n - 2)
  }
}

